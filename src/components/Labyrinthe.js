// construction du labyrinthe à partir de la map
export const construction = (scene, currentLevel) => {
    const textureLoader = new THREE.TextureLoader();

    const wallTexture = textureLoader.load('/images/wall.jpg');
    wallTexture.colorSpace = THREE.SRGBColorSpace;
    const groundTexture = textureLoader.load('/images/ground.jpg');
    groundTexture.colorSpace = THREE.SRGBColorSpace;

    const wallGeometry = new THREE.BoxGeometry(1, 1, 1);
    const wallMaterial = new THREE.MeshLambertMaterial({ map: wallTexture });

    const groundGeometry = new THREE.BoxGeometry(1, 0.1, 1);
    const groundMaterial = new THREE.MeshLambertMaterial({ map: groundTexture });

    const edgesMaterial = new THREE.LineBasicMaterial({ color: 0x1a1206 });
    const wallEdges = new THREE.EdgesGeometry(wallGeometry);
    const groundEdges = new THREE.EdgesGeometry(groundGeometry);

    for (let i = 0; i < currentLevel.length; i++) {
        for (let j = 0; j < currentLevel[i].length; j++) {
            const numCase = currentLevel[i][j];

            if (numCase === 1) {
                const wallMesh = new THREE.Mesh(wallGeometry, wallMaterial);
                wallMesh.position.set(j, 0.5, i);
                scene.add(wallMesh);

                const wallLine = new THREE.LineSegments(wallEdges, edgesMaterial);
                wallLine.position.set(j, 0.5, i);
                scene.add(wallLine);
            }

            const groundMesh = new THREE.Mesh(groundGeometry, groundMaterial);
            groundMesh.position.set(j, 0, i);
            scene.add(groundMesh);
        }
    }
};