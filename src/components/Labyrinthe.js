export const construction = (scene) => {
    const wall = new THREE.BoxGeometry(1, 1, 1);
    const wallMaterial = new THREE.MeshBasicMaterial({ color: 0x888888 });

    const ground = new THREE.BoxGeometry(1, 0.1, 1);
    const groundMaterial = new THREE.MeshBasicMaterial({ color: 0x333333 });

    const startMaterial = new THREE.MeshBasicMaterial({ color: 0x00ff00 });
    const endMaterial = new THREE.MeshBasicMaterial({ color: 0xff0000 });


    for (let i = 0; i < mapLevel1.length; i++) {
        for (let j = 0; j < mapLevel1[i].length; j++) {
            const numCase = mapLevel1[i][j];

            if (numCase === 1) {
                const wallMesh = new THREE.Mesh(wall, wallMaterial);
                wallMesh.position.set(j, 0.5, i);
                scene.add(wallMesh);
            }

            let specialColor = groundMaterial;

            if (numCase === 2) {
                specialColor = startMaterial;
            }
            else if (numCase === 3) {
                specialColor = endMaterial;
            }

            const groundMesh = new THREE.Mesh(ground, specialColor);
            groundMesh.position.set(j, 0, i);
            scene.add(groundMesh);
        }
    }
};